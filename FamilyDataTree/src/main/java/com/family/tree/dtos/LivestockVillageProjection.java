package com.family.tree.dtos;

public class LivestockVillageProjection {

    private String villageName;
    private int cow;
    private int goat;
    private int sheep;
    private int hen;
    private int buffalo;
    private int total;

    public LivestockVillageProjection() {}

    public String getVillageName() {
        return villageName;
    }

    public int getCow() {
        return cow;
    }

    public int getGoat() {
        return goat;
    }

    public int getSheep() {
        return sheep;
    }

    public int getHen() {
        return hen;
    }

    public int getBuffalo() {
        return buffalo;
    }

    public int getTotal() {
        return total;
    }
}
